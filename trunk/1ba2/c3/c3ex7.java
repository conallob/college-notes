import tcdIO.*; 

class c3ex7 {
	public static void main (String args[]) {
		Terminal terminal;
		int x, y;

		terminal = new Terminal("Chapter 3: Exercise 7");

		x = terminal.readInt("Input the value of the first variable: ");
		y = terminal.readInt("Input the value of the second variable: ");

		x = x + y;
		y = x - y;
		x = x - y;

		terminal.println("The value of the first variable is now:"+x);
		terminal.println("The value of the second variable is now:"+y);
 }
}   
